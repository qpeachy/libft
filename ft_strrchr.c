char    *ft_strrchr(const char *s, int c)
{
    int i;
    int oc;

    i = 0;
    oc = 0;
    while (s[i])
    {
        if (s[i] == c)
            oc = i;
        i++;
    }
    return ((char *)&s[oc]);
}